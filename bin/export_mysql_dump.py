import argparse
import datetime as dt
import math
from decimal import Decimal
from pathlib import Path

import mysql.connector


def parse_args():
    parser = argparse.ArgumentParser(description="Export a MySQL database without requiring mysqldump.")
    parser.add_argument("--host", required=True)
    parser.add_argument("--port", required=True, type=int)
    parser.add_argument("--user", required=True)
    parser.add_argument("--password", default="")
    parser.add_argument("--database", required=True)
    parser.add_argument("--charset", default="utf8mb4")
    parser.add_argument("--output", required=True)
    return parser.parse_args()


def quote_identifier(value):
    return "`" + value.replace("`", "``") + "`"


def escape_string(value):
    return (
        str(value)
        .replace("\\", "\\\\")
        .replace("\0", "\\0")
        .replace("\n", "\\n")
        .replace("\r", "\\r")
        .replace("\x1a", "\\Z")
        .replace("'", "\\'")
    )


def sql_literal(value):
    if value is None:
        return "NULL"
    if isinstance(value, bool):
        return "1" if value else "0"
    if isinstance(value, int):
        return str(value)
    if isinstance(value, float):
        if math.isnan(value) or math.isinf(value):
            return "NULL"
        return repr(value)
    if isinstance(value, Decimal):
        return format(value, "f")
    if isinstance(value, (dt.datetime, dt.date, dt.time)):
        return "'" + escape_string(value.isoformat(sep=" ") if isinstance(value, dt.datetime) else value.isoformat()) + "'"
    if isinstance(value, (bytes, bytearray, memoryview)):
        return "0x" + bytes(value).hex()
    return "'" + escape_string(value) + "'"


def connect(args, database=None):
    return mysql.connector.connect(
        host=args.host,
        port=args.port,
        user=args.user,
        password=args.password or None,
        database=database,
        charset=args.charset,
        use_unicode=True,
        autocommit=True,
    )


def list_base_tables(connection):
    cursor = connection.cursor()
    try:
        cursor.execute("SHOW FULL TABLES WHERE Table_type = 'BASE TABLE'")
        return [row[0] for row in cursor.fetchall()]
    finally:
        cursor.close()


def write_table_schema(connection, output, table_name):
    cursor = connection.cursor()
    try:
        cursor.execute(f"SHOW CREATE TABLE {quote_identifier(table_name)}")
        row = cursor.fetchone()
        output.write(f"DROP TABLE IF EXISTS {quote_identifier(table_name)};\n")
        output.write(f"{row[1]};\n\n")
    finally:
        cursor.close()


def write_table_rows(connection, output, table_name, batch_size=200):
    cursor = connection.cursor()
    try:
        cursor.execute(f"SELECT * FROM {quote_identifier(table_name)}")
        columns = [column[0] for column in cursor.description]
        if not columns:
            return

        quoted_columns = ", ".join(quote_identifier(column) for column in columns)
        quoted_table = quote_identifier(table_name)

        while True:
            rows = cursor.fetchmany(batch_size)
            if not rows:
                break

            value_rows = []
            for row in rows:
                value_rows.append("(" + ", ".join(sql_literal(value) for value in row) + ")")

            output.write(f"INSERT INTO {quoted_table} ({quoted_columns}) VALUES\n")
            output.write(",\n".join(value_rows))
            output.write(";\n")
        output.write("\n")
    finally:
        cursor.close()


def export_database(args):
    output_path = Path(args.output).resolve()
    output_path.parent.mkdir(parents=True, exist_ok=True)

    with connect(args, database=args.database) as connection:
        tables = list_base_tables(connection)
        with output_path.open("w", encoding="utf-8", newline="\n") as output:
            output.write("-- Generated by bin/export_mysql_dump.py\n")
            output.write(f"-- Database: {args.database}\n\n")
            output.write(f"CREATE DATABASE IF NOT EXISTS {quote_identifier(args.database)} DEFAULT CHARACTER SET {args.charset};\n")
            output.write(f"USE {quote_identifier(args.database)};\n")
            output.write("SET FOREIGN_KEY_CHECKS=0;\n")
            output.write(f"SET NAMES {args.charset};\n\n")

            for table_name in tables:
                write_table_schema(connection, output, table_name)

            for table_name in tables:
                write_table_rows(connection, output, table_name)

            output.write("SET FOREIGN_KEY_CHECKS=1;\n")


def main():
    export_database(parse_args())


if __name__ == "__main__":
    main()
